/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var swapPairs = function(head) {
    if (!head || !head.next) {
        return head
    }
    let first = head
    let second = head.next
    let result = second
    let last
    while (first && second) {
        let third = second.next
        second.next = first
        first.next = third
        if (last) {
            last.next = second
        }
        last = first
        first = first.next
        second = first ? first.next : null
    }
    return result
}

